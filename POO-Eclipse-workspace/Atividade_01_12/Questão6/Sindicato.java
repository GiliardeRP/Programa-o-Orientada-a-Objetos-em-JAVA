package Questão6;

public class Sindicato {
	private int codAssociado;
	private String processos, direitos;
	
	public int getCodAssociado() {
		return codAssociado;
	}
	public void setCodAssociado(int codAssociado) {
		this.codAssociado = codAssociado;
	}
	public String getProcessos() {
		return processos;
	}
	public void setProcessos(String processos) {
		this.processos = processos;
	}
	public String getDireitos() {
		return direitos;
	}
	public void setDireitos(String direitos) {
		this.direitos = direitos;
	}
}
